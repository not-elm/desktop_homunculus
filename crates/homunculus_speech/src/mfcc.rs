pub fn smooth_damp(
    current: f32,
    target: f32,
    current_velocity: &mut f32,
    smooth_time: f32,
    max_speed: f32,
    delta_time: f32,
) -> f32 {
    let smooth_time = smooth_time.max(0.0001);
    let omega = 2.0 / smooth_time;
    let x = omega * delta_time;
    let exp = 1.0 / (1.0 + x + 0.48 * x * x + 0.235 * x * x * x);

    // 変位の最大値を制限
    let mut change = current - target;
    let max_change = max_speed * smooth_time;
    change = change.clamp(-max_change, max_change);

    let target_temp = current - change;
    let temp = (*current_velocity + omega * change) * delta_time;
    *current_velocity = (*current_velocity - omega * temp) * exp;

    let mut output = target_temp + (change + temp) * exp;
    if (0.0 < (target - current)) == (exp < output) {
        output = target;
        *current_velocity = (output - target) / delta_time;
    }
    output
}

type Templates = Vec<(String, Mfcc)>;

type Mfcc = [f64; 12];

pub fn calc_vowels(
    fps: u32,
    reader: &mut WavReader<Cursor<Vec<u8>>>,
    templates: &Templates,
) -> VecDeque<Option<Vowel>> {
    obtain_buffers(fps, reader)
        .iter()
        .map(|mfcc| analyze_vowel(mfcc, templates))
        .collect()
}

pub fn calc_average(fps: u32, reader: &mut WavReader<Cursor<Vec<u8>>>) -> Mfcc {
    let buffers = obtain_buffers(fps, reader);
    buffers
        .iter()
        .fold([0.0; 12], |mut acc, mfcc| {
            for i in 0..12 {
                acc[i] += mfcc[i];
            }
            acc
        })
        .map(|x| x / buffers.len() as f64)
}

pub fn obtain_buffers(fps: u32, reader: &mut WavReader<Cursor<Vec<u8>>>) -> Vec<Mfcc> {
    let sample_rate = reader.spec().sample_rate;
    let samples = reader
        .samples()
        .filter_map(Result::ok)
        .map(i16::to_sample::<f64>)
        .collect::<Vec<_>>();
    let samples_per_frame = (sample_rate as f64 / fps as f64).round() as usize;
    samples
        .chunks(samples_per_frame)
        .map(|frame| compute_mfcc(frame, sample_rate as usize, 16000))
        .collect()
}

fn obtain_samples(reader: &mut WavReader<Cursor<Vec<u8>>>) -> Vec<f32> {
    match reader.spec().sample_format {
        SampleFormat::Float => reader
            .samples::<f32>()
            .filter_map(Result::ok)
            .collect::<Vec<_>>(),
        SampleFormat::Int => reader
            .samples::<i32>()
            .filter_map(Result::ok)
            .map(|s| s as f32)
            .collect::<Vec<_>>(),
    }
}

fn distance(lhs: &[f64], rhs: &[f64]) -> f64 {
    lhs.iter()
        .zip(rhs.iter())
        .map(|(a, b)| (a - b).powi(2))
        .sum::<f64>()
        .sqrt()
}

fn analyze_vowel(mfcc: &Mfcc, templates: &Templates) -> Option<Vowel> {
    let mut best_vowel = None;
    let mut max_score = 0.0;
    for (vowel, template) in templates.iter() {
        let score = calc_score(mfcc, template);
        if max_score < score {
            max_score = score;
            best_vowel.replace(Vowel {
                name: vowel.clone(),
                weight: 1.,
            });
        }
    }
    best_vowel
}

fn calc_score(mfcc: &Mfcc, phoneme: &Mfcc) -> f64 {
    let d = mfcc
        .iter()
        .zip(phoneme.iter())
        .map(|(a, b)| (a - b).powi(2))
        .sum::<f64>();
    (-(d / 12.)).powi(10)
}

fn compute_mfcc(frame: &[f64], sample_rate: usize, target_sample_rate: usize) -> [f64; 12] {
    let frame = downsample(frame, sample_rate, target_sample_rate);
    let frame = pre_emphasis(&frame, 0.97);
    let frame = hamming(&frame);
    let frame = normalize(&frame);
    let frame = fft(&frame);
    let frame = mel_filter_bank(&frame, sample_rate as f64, 30);
    let frame = frame
        .iter()
        .map(|e| 10. * e.max(1e-10).log10())
        .collect::<Vec<_>>();
    let frame = dct(&frame);
    let mut mfcc = [0.0f64; 12];
    mfcc.copy_from_slice(&frame[1..13]);

    mfcc
}

fn to_mel(freq: f64) -> f64 {
    1127.0 * (1.0 + freq / 700.0).ln()
}

fn to_hz(mel: f64) -> f64 {
    700.0 * ((mel / 1127.0).exp() - 1.0)
}

fn mel_filter_bank(spectrum: &[f64], sample_rate: f64, mel_div: usize) -> Vec<f64> {
    let mut mel_spectrum = Vec::with_capacity(spectrum.len());
    let f_max = sample_rate * 0.5;
    let mel_max = to_mel(f_max);
    let n_max = spectrum.len() / 2;
    let df = f_max / n_max as f64;
    let d_mel = mel_max / (mel_div as f64 + 1.0);

    for n in 0..mel_div {
        let mel_begin = d_mel * n as f64;
        let mel_center = d_mel * (n as f64 + 1.0);
        let mel_end = d_mel * (n as f64 + 2.0);

        let f_begin = to_hz(mel_begin);
        let f_center = to_hz(mel_center);
        let f_end = to_hz(mel_end);

        let i_begin = (f_begin / df).ceil() as usize;
        let i_center = (f_center / df).round() as usize;
        let i_end = (f_end / df).floor() as usize;

        let mut sum = 0.0;
        for i in (i_begin + 1)..=i_end.min(spectrum.len() - 1) {
            let f = df * i as f64;
            let a = if i < i_center {
                (f - f_begin) / (f_center - f_begin)
            } else {
                (f_end - f) / (f_end - f_center)
            } / ((f_end - f_begin) * 0.5);
            sum += a * spectrum[i];
        }
        mel_spectrum.push(sum);
    }
    mel_spectrum
}

fn downsample(input: &[f64], sample_rate: usize, target_sample_rate: usize) -> Vec<f64> {
    // 1. No resampling needed if the source rate is not higher
    if sample_rate <= target_sample_rate {
        return input.to_vec();
    }

    // 2. Integer-ratio decimation
    if sample_rate % target_sample_rate == 0 {
        let skip = sample_rate / target_sample_rate;
        return input.iter().step_by(skip).cloned().collect();
    }

    // 3. Non-integer ratio: linear interpolation resampling
    let src_len = input.len();
    let ratio = sample_rate as f64 / target_sample_rate as f64;
    // Determine output length by rounding
    let out_len = (src_len as f64 / ratio).round() as usize;
    let mut output = Vec::with_capacity(out_len);
    for j in 0..out_len {
        let idx = ratio * j as f64;
        let i0 = idx.floor() as usize;
        let i1 = i0.min(src_len - 1);
        let t = idx - i0 as f64;
        let v0 = input[i0];
        let v1 = input[i1];
        output.push(v0.lerp(v1, t));
    }
    output
}

pub fn pre_emphasis(input: &[f64], coe_ff: f64) -> Vec<f64> {
    let mut output = Vec::with_capacity(input.len());
    output.push(input[0]);
    for i in 1..input.len() {
        output.push(input[i] - coe_ff * input[i - 1]);
    }
    output
}

/// 正規化
pub fn normalize(input: &[f64]) -> Vec<f64> {
    let max_abs = input.iter().map(|&x| x.abs()).fold(0.0, f64::max);
    if max_abs == 0.0 {
        input.to_vec()
    } else {
        input.iter().map(|&x| x / max_abs).collect()
    }
}

/// ハミング窓をかける
pub fn hamming(input: &[f64]) -> Vec<f64> {
    let n = input.len() as f64;
    input
        .iter()
        .enumerate()
        .map(|(i, &x)| {
            if i == 0 || i + 1 == input.len() {
                0.0
            } else {
                let h = 0.54 - 0.46 * (2.0 * PI * i as f64 / (n - 1.0)).cos();
                x * h
            }
        })
        .collect()
}

fn fft(buf: &[f64]) -> Vec<f64> {
    let mut spectrum = buf
        .iter()
        .map(|re| Complex64::new(*re, 0.0))
        .collect::<Vec<_>>();
    let mut fft_planner = FftPlanner::<f64>::new();
    let fft = fft_planner.plan_fft_forward(buf.len());
    fft.process(&mut spectrum);
    spectrum.iter().map(|s| s.norm()).collect()
}

fn dct(spectrum: &[f64]) -> Vec<f64> {
    let mut out = Vec::with_capacity(spectrum.len());
    let len = spectrum.len();
    let a = (std::f32::consts::PI / len as f32) as f64;
    for i in 0..len {
        let mut sum = 0.0;
        for j in 0..len {
            let ang = (j as f64 + 0.5) * i as f64 * a;
            sum += spectrum[j] * ang.cos();
        }
        out.push(sum);
    }
    out
}

